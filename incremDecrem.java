public class incrementation {
    public static void main(String[] args) {
        int a = 10;
        
        System.out.println("a++ vai atribuir a variável e depois incrementar = " + a++);
        System.out.println("++a vai incrementar a variavel e depois atribuir = " + ++a);
        System.out.println("a-- vai atribuir a variável e depois decrementar = " + a--);
        System.out.println("--a vai decrementar a variavel e depois atribuir = " + --a);
    }
} 
